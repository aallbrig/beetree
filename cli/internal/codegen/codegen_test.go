package codegen

import (
	"testing"

	"github.com/aallbrig/beetree-cli/internal/model"
	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
)

func sampleSpec() *model.TreeSpec {
	return &model.TreeSpec{
		Version: "1.0",
		Metadata: model.Metadata{
			Name:        "enemy-ai",
			Description: "Enemy patrol and combat AI",
			Author:      "test",
		},
		Blackboard: []model.BlackboardVar{
			{Name: "target", Type: "object", Description: "Current target entity"},
			{Name: "health", Type: "float", Default: 100.0},
			{Name: "is_alerted", Type: "bool", Default: false},
		},
		CustomNodes: []model.CustomNodeDef{
			{
				Name:        "DetectNearbyEnemy",
				Type:        "condition",
				Description: "Checks for enemies within radius",
				Parameters: []model.ParameterDef{
					{Name: "detection_radius", Type: "float", Default: 15.0},
				},
				BlackboardReads:  []string{"target"},
				BlackboardWrites: []string{"target", "is_alerted"},
			},
		},
		Tree: model.NodeSpec{
			Type: "selector",
			Name: "root",
			Children: []model.NodeSpec{
				{
					Type: "sequence",
					Name: "engage",
					Children: []model.NodeSpec{
						{Type: "condition", Name: "has_target", Node: "HasTarget"},
						{Type: "action", Name: "attack", Node: "Attack"},
					},
				},
				{
					Type: "sequence",
					Name: "detect",
					Children: []model.NodeSpec{
						{Type: "condition", Name: "detect_enemy", Node: "DetectNearbyEnemy"},
						{Type: "action", Name: "alert", Node: "Alert"},
					},
				},
				{Type: "action", Name: "patrol", Node: "Patrol"},
			},
		},
	}
}

func TestCollectLeafNodes(t *testing.T) {
	spec := sampleSpec()
	leaves := CollectLeafNodes(&spec.Tree)

	assert.Len(t, leaves, 5)

	names := make([]string, len(leaves))
	for i, l := range leaves {
		names[i] = l.Name
	}
	assert.Contains(t, names, "has_target")
	assert.Contains(t, names, "attack")
	assert.Contains(t, names, "patrol")
}

func TestCollectLeafNodes_UniqueByNode(t *testing.T) {
	spec := &model.TreeSpec{
		Tree: model.NodeSpec{
			Type: "sequence",
			Name: "root",
			Children: []model.NodeSpec{
				{Type: "action", Name: "attack1", Node: "Attack"},
				{Type: "action", Name: "attack2", Node: "Attack"},
			},
		},
	}

	leaves := CollectLeafNodes(&spec.Tree)
	assert.Len(t, leaves, 2, "should return all leaves even with same Node class")
}

