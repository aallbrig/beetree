func TestBuildTemplateData(t *testing.T) {
	spec := sampleSpec()
	data := BuildTemplateData(spec)

	assert.Equal(t, "enemy-ai", data.TreeName)
	assert.Equal(t, "EnemyAi", data.TreeClassName)
	assert.Equal(t, "Enemy patrol and combat AI", data.Description)
	assert.Len(t, data.Blackboard, 3)
	assert.Len(t, data.Actions, 3)
	assert.Len(t, data.Conditions, 2)
	require.NotNil(t, data.RootNode)
}

func TestToPascalCase(t *testing.T) {
	tests := []struct {
		input    string
		expected string
	}{
		{"enemy-ai", "EnemyAi"},
		{"patrol_and_engage", "PatrolAndEngage"},
		{"simple", "Simple"},
		{"already-PascalCase", "AlreadyPascalcase"},
		{"my_tree-name", "MyTreeName"},
	}

	for _, tt := range tests {
		t.Run(tt.input, func(t *testing.T) {
			assert.Equal(t, tt.expected, ToPascalCase(tt.input))
		})
	}
}

func TestToSnakeCase(t *testing.T) {
	tests := []struct {
		input    string
		expected string
	}{
		{"EnemyAi", "enemy_ai"},
		{"HasTarget", "has_target"},
		{"BTNode", "bt_node"},
		{"simple", "simple"},
	}

	for _, tt := range tests {
		t.Run(tt.input, func(t *testing.T) {
			assert.Equal(t, tt.expected, ToSnakeCase(tt.input))
		})
	}
}

